// decimals are also known as floating point numbers or floats.
var myDecimal = 5.7;
// myDecimal is a decimal number
var ourDecimal = 2.007;

// you can also multiply decimals
var product = myDecimal * ourDecimal; // product is equal to 0.0399
console.log(product);

// you can also divide decimals
var quotient = myDecimal / ourDecimal; // quotient is equal to 2.837
console.log(quotient);
