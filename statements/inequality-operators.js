// inequality operators are used to compare two values and return a boolean value
// they look like this: !=, !==, <, >, <=, >=

let a = 10;
let b = 20;

console.log(a != b);  // true
console.log(a !== b); // true
console.log(a < b);   // true
console.log(a > b);   // false
console.log(a <= b);  // true
console.log(a >= b);  // false